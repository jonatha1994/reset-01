package atividaderpg;

public class Clerigo {
    String nome;
    int vida;
    int ataque;
    int defesa;
    int fe;

    Clerigo(String nome, int vida, int ataque, int defesa, int fe) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.fe = fe;
    }
}
