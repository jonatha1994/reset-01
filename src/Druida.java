package atividaderpg;

public class Druida {
    String nome;
    int vida;
    int ataque;
    int defesa;
    int fe;

    Druida(String nome, int vida, int ataque, int defesa, int fe) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.fe = fe;
    }
}