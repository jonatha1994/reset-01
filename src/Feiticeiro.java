package atividaderpg;

public class Feiticeiro {
    String nome;
    int vida;
    int ataque;
    int defesa;
    int mana;

    Feiticeiro(String nome, int vida, int ataque, int defesa, int mana) {
        this.nome = nome;
        this.vida = vida;
        this.ataque = ataque;
        this.defesa = defesa;
        this.mana = mana;
    }
}
